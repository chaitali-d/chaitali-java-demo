package com.chaitali._1buildingblock;

public class AssignmentOperator {
	public static void main(String[] args) {
		int x=5;
		System.out.println(x);
		x+=3;
		System.out.println(x+=3);
		x-=3;
		System.out.println(x-=3);
		x*=3;
		System.out.println(x*=3);
		x/=3;
		System.out.println(x*=3);
		x%=3;
		System.out.println(x%=3);
		x&=3;
		System.out.println(x&=3);
		x|=3;
		System.out.println(x|=3);
		x^=3;
		System.out.println(x^=3);
		x>>=3;
		System.out.println(x>>=3);
		x<<=3;
		System.out.println(x<<=3);
		
		;
		
	}

}
